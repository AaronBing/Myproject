int main(void)
{
  /* MCU Configuration--------------------------------------------------------*/

  /* 重置所有外围设备，初始化Flash界面和Systick */
  HAL_Init();                       //1、配置Flash预取功能.		2、配置SysTick的中断时间为1ms。	
																		//3、配置SysTick的中断优先级为最低		4、调用HAL_MspInit回调函数，执行全局底层硬件初始化，

  /* 配置系统时钟 */
  SystemClock_Config();

  /* 初始化所有已配置的外围设备 */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC_Init();
  MX_DMA_Init();
  MX_TIM1_Init();
  MX_USART1_UART_Init();
  MX_MotorControl_Init();

  /* 初始化中断 */
  MX_NVIC_Init();
	/* MCU Configuration-----------O---------V---------E----------R-----------*/

  while (1)
  {
			MC_StartMotor1();
  }
  
}

